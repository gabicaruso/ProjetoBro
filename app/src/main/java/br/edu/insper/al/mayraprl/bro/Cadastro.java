package br.edu.insper.al.mayraprl.bro;

public class Cadastro {
    public String name;
    public String email;
    public String id;
    public String last;
    public String date;
    public String cpf;
    public String estado;
    public String city;
    public String street;

    public Cadastro(String name, String email, String id, String last, String date, String cpf, String estado, String city, String street) {
        this.name = name;
        this.email = email;
        this.id = id;
        this.last = last;
        this.date = date;
        this.cpf = cpf;
        this.estado = estado;
        this.city = city;
        this.street = street;
    }


}
